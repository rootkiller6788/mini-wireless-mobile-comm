/**
 * test_crypto.c — Tests for cryptographic primitives
 */
#include "wireless_crypto.h"
#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <assert.h>

static int passed = 0, failed = 0;
#define TEST(n) printf("  TEST: %-45s ... ", n)
#define PASS() do { printf("PASS\n"); passed++; } while(0)
#define FAIL(m) do { printf("FAIL: %s\n", m); failed++; return; } while(0)
#define CHK(c,m) do { if (!(c)) FAIL(m); } while(0)

static void t_aes128(void) {
    TEST("AES-128 KAT (FIPS 197)");
    aes_ctx_t c; uint8_t ct[16], de[16];
    uint8_t k[16]={0x2b,0x7e,0x15,0x16,0x28,0xae,0xd2,0xa6,0xab,0xf7,0x15,0x88,0x09,0xcf,0x4f,0x3c};
    uint8_t p[16]={0x32,0x43,0xf6,0xa8,0x88,0x5a,0x30,0x8d,0x31,0x31,0x98,0xa2,0xe0,0x37,0x07,0x34};
    uint8_t e[16]={0x39,0x25,0x84,0x1d,0x02,0xdc,0x09,0xfb,0xdc,0x11,0x85,0x97,0x19,0x6a,0x0b,0x32};
    CHK(aes_key_setup(&c,k,128)==0,"setup"); aes_encrypt_block(&c,p,ct);
    CHK(memcmp(ct,e,16)==0,"encrypt"); aes_decrypt_block(&c,ct,de);
    CHK(memcmp(de,p,16)==0,"decrypt"); PASS();
}
static void t_aes256(void) {
    TEST("AES-256 KAT");
    aes_ctx_t c; uint8_t ct[16];
    uint8_t k[32]={0x60,0x3d,0xeb,0x10,0x15,0xca,0x71,0xbe,0x2b,0x73,0xae,0xf0,0x85,0x7d,0x77,0x81,0x1f,0x35,0x2c,0x07,0x3b,0x61,0x08,0xd7,0x2d,0x98,0x10,0xa3,0x09,0x14,0xdf,0xf4};
    uint8_t p[16]={0x6b,0xc1,0xbe,0xe2,0x2e,0x40,0x9f,0x96,0xe9,0x3d,0x7e,0x11,0x73,0x93,0x17,0x2a};
    uint8_t e[16]={0xf3,0xee,0xd1,0xbd,0xb5,0xd2,0xa0,0x3c,0x06,0x4b,0x5a,0x7e,0x3d,0xb1,0x81,0xf8};
    CHK(aes_key_setup(&c,k,256)==0,"setup"); aes_encrypt_block(&c,p,ct);
    CHK(memcmp(ct,e,16)==0,"encrypt"); PASS();
}
static void t_aes_invalid(void) {
    TEST("AES invalid key rejection");
    aes_ctx_t c; uint8_t k[1]={0};
    CHK(aes_key_setup(&c,k,64)==-1,"reject 64"); CHK(aes_key_setup(&c,k,192)==0,"accept 192");
    CHK(aes_key_setup(NULL,k,128)==-1,"reject NULL"); PASS();
}
static void t_sha256(void) {
    TEST("SHA-256 KAT (FIPS 180-4)");
    const char*m="abc"; uint8_t d[32];
    uint8_t e[32]={0xba,0x78,0x16,0xbf,0x8f,0x01,0xcf,0xea,0x41,0x41,0x40,0xde,0x5d,0xae,0x22,0x23,0xb0,0x03,0x61,0xa3,0x96,0x17,0x7a,0x9c,0xb4,0x10,0xff,0x61,0xf2,0x00,0x15,0xad};
    sha256_hash((const uint8_t*)m,strlen(m),d); CHK(memcmp(d,e,32)==0,"mismatch"); PASS();
}
static void t_sha256_empty(void) {
    TEST("SHA-256 empty message");
    uint8_t d[32];
    uint8_t e[32]={0xe3,0xb0,0xc4,0x42,0x98,0xfc,0x1c,0x14,0x9a,0xfb,0xf4,0xc8,0x99,0x6f,0xb9,0x24,0x27,0xae,0x41,0xe4,0x64,0x9b,0x93,0x4c,0xa4,0x95,0x99,0x1b,0x78,0x52,0xb8,0x55};
    sha256_hash(NULL,0,d); CHK(memcmp(d,e,32)==0,"empty mismatch"); PASS();
}
static void t_hmac(void) {
    TEST("HMAC-SHA256 RFC 4231 Test 1");
    uint8_t k[20]={0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b};
    const char*d="Hi There"; uint8_t m[32];
    uint8_t e[32]={0xb0,0x34,0x4c,0x61,0xd8,0xdb,0x38,0x53,0x5c,0xa8,0xaf,0xce,0xaf,0x0b,0xf1,0x2b,0x88,0x1d,0xc2,0x00,0xc9,0x83,0x3d,0xa7,0x26,0xe9,0x37,0x6c,0x2e,0x32,0xcf,0xf7};
    hmac_sha256(k,20,(const uint8_t*)d,strlen(d),m); CHK(memcmp(m,e,32)==0,"HMAC mismatch"); PASS();
}
static void t_rc4(void) {
    TEST("RC4 round-trip");
    rc4_ctx_t c; uint8_t k[5]={1,2,3,4,5}; uint8_t d[]="Hello RC4 Test!"; uint8_t b[64]; size_t l=strlen((char*)d);
    memcpy(b,d,l); rc4_init(&c,k,5); rc4_crypt(&c,b,l);
    CHK(memcmp(b,d,l)!=0,"no encrypt"); rc4_init(&c,k,5); rc4_crypt(&c,b,l);
    CHK(memcmp(b,d,l)==0,"round-trip fail"); PASS();
}
static void t_pbkdf2(void) {
    TEST("PBKDF2-HMAC-SHA256");
    const char*p="password",*s="salt"; uint8_t dk[32];
    pbkdf2_hmac_sha256((const uint8_t*)p,strlen(p),(const uint8_t*)s,strlen(s),4096,dk,32);
    CHK(dk[0]!=0||dk[1]!=0,"all-zero key"); PASS();
}
static void t_ccmp(void) {
    TEST("AES-CCMP encrypt+auth+decrypt");
    ccmp_ctx_t cc; uint8_t ct[64],de[64],tag[16];
    uint8_t k[16]={0x40,0x41,0x42,0x43,0x44,0x45,0x46,0x47,0x48,0x49,0x4a,0x4b,0x4c,0x4d,0x4e,0x4f};
    uint8_t n[13]={0x10,0x11,0x12,0x13,0x14,0x15,0x16,0x17,0x18,0x19,0x1a,0x1b,0x1c};
    const char*pt="CCMP Test"; size_t l=strlen(pt);
    CHK(aes_key_setup(&cc.aes_ctx,k,128)==0,"setup");
    CHK(ccmp_encrypt(&cc,n,(const uint8_t*)pt,l,NULL,0,ct,tag,8)==0,"encrypt");
    CHK(ccmp_decrypt(&cc,n,ct,l,NULL,0,tag,8,de)==0,"decrypt");
    CHK(memcmp(de,pt,l)==0,"plain mismatch");
    tag[0]^=0xFF; CHK(ccmp_decrypt(&cc,n,ct,l,NULL,0,tag,8,de)==-1,"tamper accept"); PASS();
}
static void t_ct_memcmp(void) {
    TEST("constant_time_memcmp");
    uint8_t a[16]={0},b[16]={0};
    CHK(constant_time_memcmp(a,b,16)==0,"equal"); b[15]=1;
    CHK(constant_time_memcmp(a,b,16)!=0,"diff"); PASS();
}
static void t_xor(void) {
    TEST("xor_bytes");
    uint8_t a[4]={0xAA,0xBB,0xCC,0xDD},b[4]={0xFF,0x00,0xFF,0x00},e[4]={0x55,0xBB,0x33,0xDD};
    xor_bytes(a,b,4); CHK(memcmp(a,e,4)==0,"xor fail"); PASS();
}
int main(void) {
    printf("=== Test: wireless_crypto ===\n");
    t_aes128(); t_aes256(); t_aes_invalid(); t_sha256(); t_sha256_empty();
    t_hmac(); t_rc4(); t_pbkdf2(); t_ccmp(); t_ct_memcmp(); t_xor();
    printf("\n=== %d passed, %d failed ===\n",passed,failed);
    return failed>0?1:0;
}
